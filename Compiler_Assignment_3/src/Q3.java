
public class Q3 {
    // Divide two numbers (I, L, F, and D), store them, and then print each result (gen3.java,program3.class)
	public static void main(String[] args) {
    	int int1 = 3;
    	int int2 = 2;
    	long long1 = 200;
    	long long2 = 100;
    	float float1 = 4.5f;
    	float float2 = 3.5f;
    	double double1 = 1.5;
    	double double2 = 0.5;
    	
    	int intResult = int1/int2;
    	long longResult = long1/long2;
    	float floatResult = float1/float2;
    	double doubleResult = double1/double2;
    	
    	System.out.println(intResult);
    	System.out.println(longResult);
    	System.out.println(floatResult);
    	System.out.println(doubleResult);
    }
}
