import java.util.Scanner;
public class Q6 {
    // Get input from the user using the Scanner class (I, L, and D) (gen6.java,program6.class)
	public static void main(String[] args) {
	    Scanner scanner = new Scanner(System.in);
	    System.out.println("Enter int");

	    int intFromUser = scanner.nextInt(); 
	    System.out.println(intFromUser);
	    
	    System.out.println("Enter long");

	    long longFromUser = scanner.nextLong(); 
	    System.out.println(longFromUser);
	    
	    System.out.println("Enter short");

	    short shortFromUser = scanner.nextShort(); 
	    System.out.println(shortFromUser);
	    
	    
	  }
}
